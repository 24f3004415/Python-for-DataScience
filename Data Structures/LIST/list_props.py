# super power of lists in python :-
# 1. Heterogeneous - it can store multiple type of data type.

lst = [1,2,3, "Python", 22.5, True]
print(lst)
print(type(lst[5]))

# 2. ordered data structure - any value can be accessed with the help of indexing
print(lst[1])

# 3. mutability - any element  in the list can be changed.
lst[2] = 30

print(lst)

